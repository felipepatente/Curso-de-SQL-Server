--DIFERENCA EM DIAS
SELECT DATEDIFF(DAY,'2015-03-12','2017-07-13')
SELECT DATEDIFF(DAY,GETDATE(),'2019-07-13')

--DIFERENCA EM MESES
SELECT DATEDIFF(MONTH,'2015-03-12','2017-07-13')
SELECT DATEDIFF(MONTH,GETDATE(),'2019-07-13')

--DIFERENCA EM ANOS
SELECT DATEDIFF(YEAR,'2015-03-12','2017-07-13')
SELECT DATEDIFF(YEAR,GETDATE(),'2019-07-13')